# ─────────────────────────────────────────────────────────────────
# Proof vs Reality (Core independent verification)
# ─────────────────────────────────────────────────────────────────

def check_proof_vs_reality(proof: dict, reality) -> CheckResult:
    """Compare proof artifact against independent test execution reality.

    This is the CORE check that detects AV4 (V tampering).
    If proof.passed != reality.passed, V was likely tampered with.
    """
    proof_passed = proof.get("passed")
    reality_passed = reality.passed
    proof_failed = proof.get("failed")
    reality_failed = reality.failed

    passed_match = proof_passed == reality_passed
    failed_match = proof_failed == reality_failed

    return CheckResult(
        rule="PROOF_VS_REALITY",
        passed=passed_match and failed_match,
        message="Proof matches independent execution" if (passed_match and failed_match) else f"AV4 ALERT: Proof says passed={proof_passed} but reality shows passed={reality_passed}",
        severity="CRITICAL",
        evidence={
            "proof": {"passed": proof_passed, "failed": proof_failed},
            "reality": {"passed": reality_passed, "failed": reality_failed},
            "passed_match": passed_match,
            "failed_match": failed_match
        }
    )
