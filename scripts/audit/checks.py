def check_av2_partial_execution(passed: int, failed: int, ignored: int) -> CheckResult:
    """AV2: Detects partial test execution (cargo test --skip)."""
    # R6 strict catches exact count mismatch
    # Additional check: if failed == 0 but passed < 226, likely skipped
    is_partial = (failed == 0 and passed < BASELINE_PASSED)

    return CheckResult(
        rule="AV2",
        passed=not is_partial,
        message="No partial execution detected" if not is_partial else f"AV2 DETECTED: Possible partial execution — only {passed}/{BASELINE_PASSED} tests run",
        severity="CRITICAL",
        evidence={"passed": passed, "baseline": BASELINE_PASSED, "missing": BASELINE_PASSED - passed}
    )


def check_av6_ignore_bypass(repo_root=".", baseline_branch="origin/develop/v2.8.0"):
    """AV6: Full repo scan for #[ignore].

    Note: Total ignore count includes grandfathered ignores from v2.7.0.
    R2 (diff-based) already catches NEW ignores. AV6 provides total count
    for visibility — it is informational, not a hard violation.
    """
    result = subprocess.run(
        ["grep", "-R", "#\\[ignore", "--include=*.rs", "tests/", "crates/"],
        capture_output=True,
        text=True,
        cwd=repo_root
    )
    all_ignores = [l for l in result.stdout.splitlines() if l.strip()]

    return CheckResult(
        rule="AV6",
        passed=True,  # Informational only — R2 catches new ignores
        message=f"Found {len(all_ignores)} #[ignore] (grandfathered from v2.7.0; R2 monitors for new)",
        severity="INFO",
        evidence={
            "total_count": len(all_ignores),
            "note": "Total ignore count. R2 diff-check catches new ignores. AV6 is for visibility."
        }
    ), {"total": len(all_ignores)}


def check_av9_determinism(runs: list) -> CheckResult:
    """AV9: Non-deterministic test masking (requires N>=2 runs)."""
    if len(runs) < 2:
        return CheckResult(
            rule="AV9",
            passed=True,
            message="UNDETERMINED: Single run cannot detect non-determinism",
            severity="MEDIUM",
            evidence={"runs": len(runs), "note": "Run with --runs 3 for determinism check"}
        )

    first = runs[0]
    inconsistent = []
    for i, r in enumerate(runs[1:], 1):
        if r.passed != first.passed or r.failed != first.failed or r.ignored != first.ignored:
            inconsistent.append(i + 1)

    consistent = len(inconsistent) == 0

    return CheckResult(
        rule="AV9",
        passed=consistent,
        message="Deterministic" if consistent else f"AV9 DETECTED: Runs {inconsistent} differ from run 1",
        severity="MEDIUM",
        evidence={"run_count": len(runs), "inconsistent_runs": inconsistent, "run_details": [{"run": i+1, "passed": r.passed, "failed": r.failed} for i, r in enumerate(runs)]}
    )


