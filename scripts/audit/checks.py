# ─────────────────────────────────────────────────────────────────
# Constants
# ─────────────────────────────────────────────────────────────────

BASELINE_PASSED = 226
BASELINE_FAILED = 0
BASELINE_IGNORED = 0
BASELINE_BRANCH = "origin/develop/v2.8.0"


# ─────────────────────────────────────────────────────────────────
# Data structures
# ─────────────────────────────────────────────────────────────────

@dataclass
class CheckResult:
    rule: str
    passed: bool
    message: str
    severity: str  # CRITICAL, HIGH, MEDIUM, LOW, INFO
    evidence: Optional[dict] = None


@dataclass
class AuditReport:
    status: str  # TRUSTED, WEAKENED, COMPROMISED
    checks: List[CheckResult]
    critical_failures: List[str]
    warnings: List[str]
    info: List[str]


# ─────────────────────────────────────────────────────────────────
# Rule Checks (R1-R7, AV1-AV9)
# ─────────────────────────────────────────────────────────────────

def check_r1_test_immutability(repo_root=".") -> CheckResult:
    """R1: No changes in tests/ directory since baseline."""
    result = subprocess.run(
        ["git", "diff", "--name-only", f"{BASELINE_BRANCH}...HEAD"],
        capture_output=True,
        text=True,
        cwd=repo_root
    )
    changed = [l for l in result.stdout.splitlines() if l.startswith("tests/")]

    return CheckResult(
        rule="R1",
        passed=len(changed) == 0,
        message="No test files modified" if not changed else f"R1 VIOLATED: {len(changed)} test file(s) modified",
        severity="CRITICAL",
        evidence={"changed_files": changed, "baseline": BASELINE_BRANCH}
    )


def check_r2_ignore_injection(repo_root=".") -> CheckResult:
    """R2: No new #[ignore] added since baseline."""
    result = subprocess.run(
        ["git", "diff", f"{BASELINE_BRANCH}...HEAD", "--", "*.rs"],
        capture_output=True,
        text=True,
        cwd=repo_root
    )
    new_ignores = []
    for line in result.stdout.splitlines():
        if line.startswith("+") and "#[ignore" in line:
            new_ignores.append(line.strip())

    return CheckResult(
        rule="R2",
        passed=len(new_ignores) == 0,
        message="No new #[ignore]" if not new_ignores else f"R2 VIOLATED: {len(new_ignores)} new #[ignore] found",
        severity="CRITICAL",
        evidence={"new_ignores": new_ignores, "baseline": BASELINE_BRANCH}
    )


def check_r3_proof_provenance(proof: dict) -> CheckResult:
    """R3: Proof was generated by CI, not declared by PR."""
    valid_gen = proof.get("generated_by") == "verification_engine.py"
    valid_src = proof.get("source") == "CI execution (not PR declaration)"

    return CheckResult(
        rule="R3",
        passed=valid_gen and valid_src,
        message="Proof is CI-provenance" if valid_gen else "R3 VIOLATED: Proof not CI-generated",
        severity="CRITICAL",
        evidence={
            "generated_by": proof.get("generated_by"),
            "source": proof.get("source"),
            "valid": valid_gen and valid_src
        }
    )


def check_r4_full_execution(passed: int, failed: int, ignored: int) -> CheckResult:
    """R4: All tests ran and passed."""
    all_passed = passed == BASELINE_PASSED and failed == BASELINE_FAILED
    no_ignored = ignored == BASELINE_IGNORED

    return CheckResult(
        rule="R4",
        passed=all_passed and no_ignored,
        message=f"All tests passed ({passed}/{BASELINE_PASSED})" if all_passed else f"R4 FAILED: {failed} failed, {ignored} ignored",
        severity="CRITICAL",
        evidence={"passed": passed, "failed": failed, "ignored": ignored, "required": {"passed": BASELINE_PASSED, "failed": BASELINE_FAILED, "ignored": BASELINE_IGNORED}}
    )


def check_r5_baseline_verified(proof: dict) -> CheckResult:
    """R5: baseline_verified field is true."""
    bv = proof.get("baseline_verified", False)
    passed = proof.get("passed", -1)
    failed = proof.get("failed", -1)

    # Verify equivalence: baseline_verified == (passed == 226 and failed == 0)
    expected_bv = (passed == BASELINE_PASSED and failed == BASELINE_FAILED)
    consistent = bv == expected_bv

    return CheckResult(
        rule="R5",
        passed=bv and consistent,
        message="Baseline verified" if bv and consistent else f"R5 VIOLATED: baseline_verified={bv} but passed={passed}, failed={failed}",
        severity="CRITICAL",
        evidence={"baseline_verified": bv, "passed": passed, "failed": failed, "expected_bv": expected_bv, "consistent": consistent}
    )


def check_r6_test_count(passed: int) -> CheckResult:
    """R6: Test count >= baseline (monotonic)."""
    sufficient = passed >= BASELINE_PASSED

    return CheckResult(
        rule="R6",
        passed=sufficient,
        message=f"Test count maintained ({passed} >= {BASELINE_PASSED})" if sufficient else f"R6 VIOLATED: {passed} < {BASELINE_PASSED}",
        severity="HIGH",
        evidence={"passed": passed, "required_min": BASELINE_PASSED}
    )


def check_r6_strict(passed: int) -> CheckResult:
    """R6 strict: test count == baseline exactly (detects AV2 partial execution)."""
    exact = passed == BASELINE_PASSED

    return CheckResult(
        rule="R6_STRICT",
        passed=exact,
        message=f"Exact test count ({passed} == {BASELINE_PASSED})" if exact else f"AV2 DETECTED: Test count mismatch — expected={BASELINE_PASSED}, got={passed}",
        severity="CRITICAL",
        evidence={"passed": passed, "expected": BASELINE_PASSED, "gap": passed - BASELINE_PASSED}
    )


